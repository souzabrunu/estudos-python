a = input('Digite algo: ')
print('O tipo primitivo desse valor é: ',type(a))
#informa se o valor é inteiro, boolean, strings etc.
print ( 'Só tem espaços?', a.isspace())
#informa se só existe espaço.
print('É um numero? ', a.isnumeric())
# informa se é um numero.
print ('É alfabético?', a.isalpha())
#informa se é alfabetico.
print('É alfanumérico?', a.isalnum())
#informa se é alfanumerico.
print('Esta em maiusculas?', a.isupper())
# informa se é tudo maiusculas.
print('Esta em minusculas?', a.islower())
#informa se é tudo minusculas.
print('Esta capitalizada?', a.istitle())
#informa se a primeira letra é maiuscula.

